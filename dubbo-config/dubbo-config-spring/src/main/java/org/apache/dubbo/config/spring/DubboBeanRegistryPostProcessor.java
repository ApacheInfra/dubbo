package org.apache.dubbo.config.spring;

import org.apache.dubbo.config.spring.util.DubboBeanUtils;
import org.springframework.beans.BeansException;
import org.springframework.beans.factory.config.ConfigurableListableBeanFactory;
import org.springframework.beans.factory.support.BeanDefinitionRegistry;
import org.springframework.beans.factory.support.BeanDefinitionRegistryPostProcessor;

/**
 * Register Dubbo beans
 */
public class DubboBeanRegistryPostProcessor implements BeanDefinitionRegistryPostProcessor {

    public static String BEAN_NAME = "dubboBeanRegistryPostProcessor";

    private BeanDefinitionRegistry registry;

    @Override
    public void postProcessBeanDefinitionRegistry(BeanDefinitionRegistry registry) throws BeansException {
        this.registry = registry;
    }

    @Override
    public void postProcessBeanFactory(ConfigurableListableBeanFactory beanFactory) throws BeansException {

        //Register this post-processor bean as late as possible, make sure call after PropertyPlaceholderConfigurer and PropertySourcesPlaceholderConfigurer
        DubboBeanUtils.registerInfrastructureBean(registry, DubboConfigInitializationPostProcessor.BEAN_NAME, DubboConfigInitializationPostProcessor.class);

    }
}
